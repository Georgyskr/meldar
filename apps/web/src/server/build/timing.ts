export const SECOND_MS = 1000
export const MINUTE_MS = 60 * SECOND_MS

export const PIPELINE_HARD_TIMEOUT_MS = 5 * MINUTE_MS

export const PIPELINE_HEARTBEAT_INTERVAL_MS = 30 * SECOND_MS

export const STALE_HEARTBEAT_THRESHOLD_MS = PIPELINE_HARD_TIMEOUT_MS

export const PIPELINE_SOFT_DEADLINE_MS = PIPELINE_HARD_TIMEOUT_MS - 20 * SECOND_MS

export const PIPELINE_EVENTS_POLL_INTERVAL_MS = 3 * SECOND_MS

export const PIPELINE_EVENTS_MAX_WAIT_MS = 10 * MINUTE_MS

export const PIPELINE_EVENTS_READ_LIMIT = 500

export const BUILD_STREAMING_REAPER_AFTER_MS = 30 * MINUTE_MS

export const PIPELINE_EVENT_RETENTION_MS = 30 * 24 * 60 * MINUTE_MS

export const LLM_CALL_SOFT_TIMEOUT_MS = 3 * MINUTE_MS

export const MAX_ERROR_REASON_LEN = 2000
export const MAX_ERROR_CODE_LEN = 64

if (PIPELINE_HEARTBEAT_INTERVAL_MS >= STALE_HEARTBEAT_THRESHOLD_MS / 3) {
	throw new Error(
		'[timing] PIPELINE_HEARTBEAT_INTERVAL_MS must fire at least 3× before stale threshold',
	)
}
if (STALE_HEARTBEAT_THRESHOLD_MS > PIPELINE_HARD_TIMEOUT_MS) {
	throw new Error('[timing] STALE_HEARTBEAT_THRESHOLD_MS must not exceed PIPELINE_HARD_TIMEOUT_MS')
}
if (PIPELINE_SOFT_DEADLINE_MS >= PIPELINE_HARD_TIMEOUT_MS) {
	throw new Error(
		'[timing] PIPELINE_SOFT_DEADLINE_MS must fire strictly before PIPELINE_HARD_TIMEOUT_MS',
	)
}
if (PIPELINE_EVENTS_POLL_INTERVAL_MS < SECOND_MS) {
	throw new Error('[timing] PIPELINE_EVENTS_POLL_INTERVAL_MS must be ≥ 1s')
}
if (PIPELINE_EVENTS_MAX_WAIT_MS <= PIPELINE_HARD_TIMEOUT_MS) {
	throw new Error(
		'[timing] PIPELINE_EVENTS_MAX_WAIT_MS must exceed PIPELINE_HARD_TIMEOUT_MS (client must outlive server)',
	)
}
